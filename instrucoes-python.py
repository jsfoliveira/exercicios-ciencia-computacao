# , o que está acontecendo é que quando você faz só pip Install o seu sistema operacional está usando a versão 2 do python, por essa razão, toda vez que você for usar esse comando (pip install) vai ser necessário colocar python3 -m, antes do próprio pip install.
# Assim os comandos para você executar aí seriam esses:
# python3 -m pip install flake8
# python3 -m pip install black
# python3 -m pip install wheel
